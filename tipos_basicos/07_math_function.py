import math
# Redondear un numero al mas cercano
print(round(1.3))
print(round(2.3))

# Valor absoluto
print(abs(-45))
print(abs(35))

# Redondear un numero hacia arriba
print(math.ceil(1.1))

# Redondear un numero hacia abajo
print(math.floor(3.99))

# Verifica si el argumento no es un numero, regresa False si es un numero
print(math.isnan(12))

# Eleva a una potencia
print(math.pow(12, 4))

# Raiz cuadrada
print(math.sqrt(9))
